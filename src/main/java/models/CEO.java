package models;

public class CEO {
    private int id;
    private String name;

    public CEO () {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(int name) {
        this.name = String.valueOf(name);
    }
}


